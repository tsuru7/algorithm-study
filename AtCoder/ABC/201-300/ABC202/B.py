def readinput():
    s = input()
    return s

def main(s):
    s = list(s[::-1])
    for i in range(len(s)):
        if s[i] == '6':
            s[i] = '9'
        elif s[i] == '9':
            s[i] = '6'

    ans=''.join(s)
    return ans

def printans(ans):
    print(ans)

if __name__=='__main__':
    s=readinput()
    ans=main(s)
    printans(ans)
